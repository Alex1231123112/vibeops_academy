# ОШИБКА 1: input() возвращает строку, нельзя складывать с числом
# age = input("Сколько вам лет? ")
# next_year = age + 1  # TypeError!
# ИСПРАВЛЕНИЕ:
age = int(input("Сколько вам лет? "))
next_year = age + 1
print(f"В следующем году вам будет {next_year}")

# ОШИБКА 2: конкатенация строки и числа
# result = "Возраст: " + age  # TypeError!
# ИСПРАВЛЕНИЕ:
result = f"Возраст: {age}"  # f-string
print(result)

# ОШИБКА 3: деление на ноль (AI иногда забывает проверять)
a = 10
b = 0
# quotient = a / b  # ZeroDivisionError!
# ИСПРАВЛЕНИЕ:
if b != 0:
    quotient = a / b
    print(f"Результат: {quotient}")
else:
    print("Деление на ноль невозможно!")
